void main() {
  int count = 0;

  // O loop while continua executando enquanto a condição for verdadeira
  while (count < 5) {
    print('O valor de count é: $count');
    count++; // Incrementa o valor de count em 1 a cada iteração
  }
}
// O loop while continua executando enquanto a condição for verdadeira
